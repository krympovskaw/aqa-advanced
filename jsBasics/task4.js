// 4.1 Площа круга
const radiusCircle = 7;
const circleArea = Math.PI * Math.pow(radiusCircle, 2);
console.log("Площа круга:", circleArea.toFixed(2));

// 4.2 Площа прямокутника
const length = 20;
const width = 30;
const rectArea = length * width;
console.log("Площа прямокутника:", rectArea.toFixed(2));

// 4.3 Об'єм циліндра
const radiusCylinder = 5;
const heightCylinder = 50;
const cylinderVolume = Math.PI * Math.pow(radiusCylinder, 2) * heightCylinder;
console.log("Об'єм циліндра", cylinderVolume.toFixed(2));
